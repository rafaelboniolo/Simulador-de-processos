    public void sTart(){
    
        t1 = new Thread(()->{
            this.jLabel12.setText("Andamento");
            for(int i = 0; i< 201; i++){
                this.bars.get(0).setValue(i);
                
                if(i == 200)
                    this.jLabel12.setText("Pronto");
                
                try { 
                    Thread.sleep(new Random().nextInt(this.sleep)); 
                } catch (Exception ex) {}
            }
        });
        t2 = new Thread(()->{
            this.jLabel13.setText("Andamento");
            for(int i = 0; i< 201; i++){
                this.bars.get(1).setValue(i);
                
                if(i == 200)
                    this.jLabel13.setText("Pronto");
                
                try { 
                    Thread.sleep(new Random().nextInt(this.sleep)); 
                } catch (Exception ex) {}
            }
        });
            
        
        t3 = new Thread(()->{
            this.jLabel14.setText("Andamento");
            for(int i = 0; i< 201; i++){
                this.bars.get(2).setValue(i);
                
                if(i == 200)
                    this.jLabel14.setText("Pronto");
                
                try { 
                    Thread.sleep(new Random().nextInt(this.sleep)); 
                } catch (Exception ex) {}
            }
        });
        
        t4 = new Thread(()->{
            this.jLabel15.setText("Andamento");
            for(int i = 0; i< 201; i++){
                this.bars.get(3).setValue(i);
                
                if(i == 200)
                    this.jLabel15.setText("Pronto");
                
                try { 
                    Thread.sleep(new Random().nextInt(this.sleep)); 
                } catch (Exception ex) {}
            }
        }); 
        
        t1.setPriority(1);
        t2.setPriority(3);
        t3.setPriority(5);
        t4.setPriority(7);
        
        t1.start();
        t2.start();
        t3.start();
        t4.start();
        
        
        
        
        }
    
    
    
    
   
